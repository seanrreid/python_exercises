"""
For vs. While

'for' loops will iterate through a collection (list, dictionary, etc...).
'while' loops continue to loop until a condition is False.
"""

power_rangers = ["Jason","Trini","Billy","Zack","Kim"]

# for item in items 
# for [SINGULAR OF ITEM] in [COLLECTION OF ITEMS]

for ranger in power_rangers:
    print(ranger)

